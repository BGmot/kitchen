<h3>What is threaded discussion forum?</h3>
Most of the forums on the Internet are powered by <a href="https://www.phpbb.com/">PHP BB software</a>, which is 'flat'. The main issue with such type of interface is
that when conversations continue it becomes increasingly harder to follow individual threads as they are represented <a href="https://s3.amazonaws.com/vipvip.ca/RX85Zpg2ZX2040-8740-1-PB.png"> by multiple nested quotes from the previous posts.</a> 
Nested quotes are hard to follow and even harder to respond to.
Unlike a flat UI, this engine incorporates visual threads that help a user follow the discussion with ease 
Branches are created automatically and all it takes is to simply reply to the right post. There is also support for real time WhatsApp-like instant messaging, built-in image gallery, 
YouTube, Facebook video, Twitter, and so much more.
