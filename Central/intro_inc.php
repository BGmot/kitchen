<h3>What is threaded discussion forum?</h3>
Most of the forums on the Internet are powered by <a href="https://www.phpbb.com/">PHP BB software</a>, which is 'flat'. 
The main issue with a flat interface is
that longer conversations become increasingly harder to follow as they are 
rendered <a href="https://s3.amazonaws.com/vipvip.ca/RX85Zpg2ZX2040-8740-1-PB.png"> as multiple nested quotes 
from previous posts.</a> 
This engine displays conversations as trees of clickable title threads instead.
As participants reply to posts, the branches are created automatically 
There is also support for real time WhatsApp-like instant messaging, built-in image gallery, 
YouTube and Facebook video, Twitter, and so much more.
